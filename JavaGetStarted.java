//Configuracion para Windows
//Para instalar Java en Windows:

//1. Vaya a "Propiedades del sistema" (Se puede encontrar en Panel de control > Sistema y seguridad > Sistema > Configuracion avanzada del sistema)
//2. Haga clic en el boton "Variables de entorno" en la pestana "Avanzado"
//3. Luego, seleccione la variable "Ruta" en Variables del sistema y haga clic en el boton "Editar"
//4. Haga clic en el boton "Nuevo" y agregue la ruta donde esto instalado Java, seguido de \bin . De forma predeterminada, 
//Java se instala en C:\Program Files\Java\jdk-11.0.1 (si no se especifico nada mas cuando lo instalo). En ese caso, debera agregar una nueva ruta con: C:\Archivos de programa\Java\jdk-11.0.1\bin
//Luego, haga clic en "Aceptar" y guarde la configuracion
//6. Por ultimo, abra el simbolo del sistema (cmd.exe) y escriba java -version para ver si Java se esta ejecutando en su maquina
